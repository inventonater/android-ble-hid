package com.inventonater.hid.core.api

/**
 * Enum representing mouse buttons.
 * Used for specifying which button to press, click, etc.
 */
enum class MouseButton {
    /**
     * Left mouse button (primary button)
     */
    LEFT,
    
    /**
     * Right mouse button (secondary button)
     */
    RIGHT,
    
    /**
     * Middle mouse button (scroll wheel button)
     */
    MIDDLE
}
